def summarize_active_parents(regime_id: int, active_parents_path: Path) -> list[dict[str, Any]]:
    if not active_parents_path.exists():
        return []
    parents = pd.read_csv(active_parents_path)
    rows: list[dict[str, Any]] = []
    for row in parents.itertuples(index=False):
        rows.append(
            {
                "regime": int(regime_id),
                "source": row.source,
                "target": row.target,
                "significant_lags": row.significant_lags,
                "strongest_lag": int(row.strongest_lag),
                "max_abs_mci": float(row.max_abs_mci),
                "signed_mci_at_strongest_lag": float(row.signed_mci_at_strongest_lag),
                "min_p_value": float(row.min_p_value),
                "sign": row.sign,
                "lag_count": int(row.lag_count),
            }
        )
    return rows


def write_markdown_summary(output_dir: Path, summary_df: pd.DataFrame, active_parent_df: pd.DataFrame) -> None:
    lines = [
        "# K=7 Target-Regime-Conditioned PCMCI Experiment",
        "",
        "## Semantics",
        "",
        "For each regime r, PCMCI samples are selected by `regime(t)=r` at the target timestamp only. "
        "Lagged variables are taken from the original continuous train timeline and are not required to share the target regime.",
        "",
        "## Regime Summary",
        "",
        "| Regime | Target rows | Rows used | Segments | Raw edges | Prior edges | Top-K edges | Mask density |",
        "|---:|---:|---:|---:|---:|---:|---:|---:|",
    ]
    for row in summary_df.itertuples(index=False):
        lines.append(
            f"| {int(row.regime)} | {int(row.target_regime_rows_in_pcmci_segments)} | "
            f"{int(row.rows_used_in_pcmci)} | {int(row.continuous_segments_kept)} | "
            f"{int(row.raw_significant_lag_edges)} | {int(row.prior_allowed_lag_edges)} | "
            f"{int(row.aggregated_edges_after_topk)} | {float(row.mask_density):.2%} |"
        )

    lines.extend(["", "## Active_Pow Parents", ""])
    if active_parent_df.empty:
        lines.append("No Active_Pow parent edges were retained after prior and Top-K filtering.")
    else:
        lines.extend(
            [
                "| Regime | Source | Lags | Strongest lag | |MCI| | p-value | Sign |",
                "|---:|---|---|---:|---:|---:|---|",
            ]
        )
        for row in active_parent_df.itertuples(index=False):
            lines.append(
                f"| {int(row.regime)} | {row.source} | {row.significant_lags} | "
                f"{int(row.strongest_lag)} | {float(row.max_abs_mci):.4f} | "
                f"{float(row.min_p_value):.3g} | {row.sign} |"
            )

    (output_dir / "regime_target_pcmci_experiment.md").write_text(
        "\n".join(lines) + "\n",
        encoding="utf-8",
    )


def main() -> None:
    args = parse_args()
    validate_arguments(args)

    project_root = find_project_root()
    train_path = resolve_repo_path(args.regime_train_path, project_root)
    output_dir = resolve_repo_path(args.output_dir, project_root)

    print(f"Loading regime-labeled train split only: {train_path}")
    train_df, time_col, rows_loaded = load_train_data(train_path)
    validate_required_columns(train_df, VARIABLES)
    if args.regime_col not in train_df.columns:
        raise KeyError(f"Missing required regime column: {args.regime_col}")

    required_columns = VARIABLES + [args.regime_col]
    missing_mask = train_df[required_columns].isna().any(axis=1)
    dropped_missing_rows = int(missing_mask.sum())
    if dropped_missing_rows:
        print(f"Dropping {dropped_missing_rows} rows with missing PCMCI/regime columns before segment detection.")
    pcmci_source_df = train_df.loc[~missing_mask].copy()
    selected_rows = len(pcmci_source_df)
    if pcmci_source_df.empty:
        raise ValueError("No usable rows remain after dropping missing values.")

    segments, all_segment_lengths, dropped_short_segments = split_continuous_segments(
        pcmci_source_df,
        time_col=time_col,
        freq_minutes=args.freq_minutes,
        min_segment_length=args.min_segment_length,
    )

    summary_rows: list[dict[str, Any]] = []
    active_parent_rows: list[dict[str, Any]] = []
    for regime_id in args.regimes:
        start_time = time.time()
        target_regime_rows_raw = int((pcmci_source_df[args.regime_col].astype(int) == int(regime_id)).sum())
        if target_regime_rows_raw <= 0:
            raise ValueError(f"No target rows found for regime {regime_id}.")

        regime_output_dir = output_dir / f"regime_{regime_id}"
        print(
            f"Running target-regime-conditioned PCMCI for regime {regime_id} "
            f"(target rows={target_regime_rows_raw}, tau={args.tau_min}..{args.tau_max})"
        )

        dataframe, prepared = prepare_regime_masked_dataframe(
            segments=segments,
            variables=VARIABLES,
            regime_col=args.regime_col,
            regime_id=regime_id,
            dropped_missing_rows=dropped_missing_rows,
        )
        results = run_target_masked_pcmci(
            dataframe=dataframe,
            tau_min=args.tau_min,
            tau_max=args.tau_max,
            pc_alpha=args.pc_alpha,
            alpha_level=args.alpha_level,
            fdr_method=args.fdr_method,
            mask_type=args.mask_type,
        )

        extracted_edges = extract_significant_edges(
            results=results,
            variables=VARIABLES,
            alpha_level=args.alpha_level,
            tau_min=args.tau_min,
            tau_max=args.tau_max,
            fdr_method=args.fdr_method,
        )
        aggregated_edges = aggregate_lag_edges(extracted_edges.prior_allowed_lag_edges)
        topk_edges = apply_topk_filter(aggregated_edges, topk_active=args.topk_active, topk_other=args.topk_other)
        adjacency = build_adjacency_matrix(topk_edges, VARIABLES)
        binary_mask, additive_mask = build_attention_masks(adjacency)

        elapsed_seconds = time.time() - start_time
        config = build_regime_config(
            args=args,
            regime_id=regime_id,
            regime_output_dir=regime_output_dir,
            train_path=train_path,
            time_col=time_col,
            rows_loaded=rows_loaded,
            selected_rows=selected_rows,
            target_regime_rows_raw=target_regime_rows_raw,
            total_segments_after_filter=len(all_segment_lengths),
            dropped_short_segments=dropped_short_segments,
            all_segment_lengths=all_segment_lengths,
            prepared=prepared,
            raw_significant_count=extracted_edges.raw_significant_count,
            filtered_by_prior_count=extracted_edges.filtered_by_prior_count,
            prior_allowed_count=len(extracted_edges.prior_allowed_lag_edges),
            aggregated_count=len(aggregated_edges),
            topk_count=len(topk_edges),
            effective_p_matrix_source=extracted_edges.effective_p_matrix_source,
            runtime_seconds=elapsed_seconds,
        )

        paths = save_outputs(
            output_dir=regime_output_dir,
            results=results,
            fdr_method=args.fdr_method,
            raw_lag_edges=extracted_edges.raw_lag_edges,
            prior_allowed_lag_edges=extracted_edges.prior_allowed_lag_edges,
            aggregated_edges=aggregated_edges,
            topk_edges=topk_edges,
            adjacency=adjacency,
            binary_mask=binary_mask,
            additive_mask=additive_mask,
            config=config,
            variables=VARIABLES,
        )

        mask_density = float(np.sum(binary_mask > 0.0) / binary_mask.size)
        summary_rows.append(
            {
                "regime": int(regime_id),
                "target_regime_rows_raw": int(target_regime_rows_raw),
                "target_regime_rows_in_pcmci_segments": int(prepared["target_regime_rows_in_segments"]),
                "rows_used_in_pcmci": int(prepared["rows_used_in_pcmci"]),
                "continuous_segments_kept": int(prepared["continuous_segments_kept"]),
                "raw_significant_lag_edges": int(extracted_edges.raw_significant_count),
                "physical_prior_filtered_lag_edges": int(extracted_edges.filtered_by_prior_count),
                "prior_allowed_lag_edges": int(len(extracted_edges.prior_allowed_lag_edges)),
                "aggregated_edges_before_topk": int(len(aggregated_edges)),
                "aggregated_edges_after_topk": int(len(topk_edges)),
                "mask_density": mask_density,
                "runtime_seconds": float(elapsed_seconds),
                "output_dir": str(regime_output_dir),
            }
        )
        active_parent_rows.extend(summarize_active_parents(regime_id, paths["active_parents"]))

    output_dir.mkdir(parents=True, exist_ok=True)
    summary_df = pd.DataFrame(summary_rows)
    active_parent_df = pd.DataFrame(active_parent_rows)
    summary_df.to_csv(output_dir / "regime_pcmci_summary.csv", index=False)
    active_parent_df.to_csv(output_dir / "regime_active_pow_parents_summary.csv", index=False)
    write_markdown_summary(output_dir, summary_df, active_parent_df)

    print(f"Finished target-regime-conditioned PCMCI outputs under: {output_dir}")


if __name__ == "__main__":
    main()
