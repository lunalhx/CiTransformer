def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Build target-regime-conditioned PCMCI graphs from K=7 GMM-HMM regime labels. "
            "For each regime r, the target timestamp is conditioned on regime(t)=r while lagged variables "
            "come from the original continuous timeline."
        )
    )
    parser.add_argument(
        "--regime_train_path",
        default=DEFAULT_REGIME_TRAIN_PATH,
        help="Path to train_with_regime.csv generated by scripts/causal/run_gmm_hmm_daytime_regimes.py.",
    )
    parser.add_argument("--regime_col", default="regime", help="Regime label column.")
    parser.add_argument("--regimes", nargs="+", type=int, default=[1, 2, 3, 4, 5, 6, 7])
    parser.add_argument("--tau_min", type=int, default=1)
    parser.add_argument("--tau_max", type=int, default=12)
    parser.add_argument("--pc_alpha", type=float, default=0.05)
    parser.add_argument("--alpha_level", type=float, default=0.05)
    parser.add_argument("--fdr_method", type=str, default="fdr_bh")
    parser.add_argument("--topk_active", type=int, default=5)
    parser.add_argument("--topk_other", type=int, default=3)
    parser.add_argument("--min_segment_length", type=int, default=24)
    parser.add_argument("--freq_minutes", type=int, default=5)
    parser.add_argument(
        "--mask_type",
        default="y",
        choices=["y"],
        help="Tigramite mask type. Keep 'y' so only target timestamps are regime-conditioned.",
    )
    parser.add_argument("--output_dir", default=DEFAULT_OUTPUT_DIR)
    return parser.parse_args()


def validate_arguments(args: argparse.Namespace) -> None:
    if args.tau_min < 0:
        raise ValueError("--tau_min must be >= 0.")
    if args.tau_max < args.tau_min:
        raise ValueError("--tau_max must be >= --tau_min.")
    if args.tau_max <= 0:
        raise ValueError("--tau_max must be positive.")
    if args.min_segment_length <= args.tau_max:
        raise ValueError("--min_segment_length should be greater than --tau_max.")
    if not 0 < args.pc_alpha <= 1:
        raise ValueError("--pc_alpha must be in (0, 1].")
    if not 0 < args.alpha_level <= 1:
        raise ValueError("--alpha_level must be in (0, 1].")
    if any(regime <= 0 for regime in args.regimes):
        raise ValueError("--regimes should contain daytime regimes only; regime 0 is night and is not supported.")


def prepare_regime_masked_dataframe(
    segments: list[pd.DataFrame],
    variables: list[str],
    regime_col: str,
    regime_id: int,
    dropped_missing_rows: int,
) -> tuple[pp.DataFrame, dict[str, Any]]:
    if any(segment[variables + [regime_col]].isna().any(axis=None) for segment in segments):
        raise ValueError("Missing values must be dropped before segment detection.")

    scaler_source = pd.concat([segment.loc[:, variables] for segment in segments], axis=0)
    std = scaler_source.std(ddof=0)
    constant_columns = [column for column in variables if pd.isna(std[column]) or np.isclose(std[column], 0.0)]
    if constant_columns:
        raise ValueError(
            "PCMCI variables contain constants after row filtering, which makes ParCorr ill-conditioned.\n"
            f"Constant columns: {constant_columns}"
        )

    scaler = StandardScaler()
    scaler.fit(scaler_source.loc[:, variables].to_numpy(dtype=np.float64))

    segment_dict: dict[int, np.ndarray] = {}
    mask_dict: dict[int, np.ndarray] = {}
    segment_lengths: list[int] = []
    target_regime_rows_in_segments = 0
    for segment_id, segment in enumerate(segments):
        values = scaler.transform(segment.loc[:, variables].to_numpy(dtype=np.float64)).astype(np.float64)
        target_mask_1d = (segment[regime_col].to_numpy(dtype=np.int64) != int(regime_id))
        mask = np.repeat(target_mask_1d[:, None], len(variables), axis=1)
        if values.shape[0] > 0:
            segment_dict[segment_id] = values
            mask_dict[segment_id] = mask.astype(bool)
            segment_lengths.append(int(values.shape[0]))
            target_regime_rows_in_segments += int((~target_mask_1d).sum())

    if target_regime_rows_in_segments <= 0:
        raise ValueError(f"No target rows for regime {regime_id} remain after segment filtering.")

    if len(segment_dict) == 1:
        analysis_mode = "single"
        data_arrays: dict[int, np.ndarray] | np.ndarray = next(iter(segment_dict.values()))
        mask_arrays: dict[int, np.ndarray] | np.ndarray = next(iter(mask_dict.values()))
    else:
        analysis_mode = "multiple"
        data_arrays = segment_dict
        mask_arrays = mask_dict

    dataframe = pp.DataFrame(data_arrays, mask=mask_arrays, var_names=variables, analysis_mode=analysis_mode)
    metadata = {
        "analysis_mode": analysis_mode,
        "rows_used_in_pcmci": int(sum(segment_lengths)),
        "continuous_segments_kept": int(len(segment_lengths)),
        "segment_lengths_kept": segment_lengths,
        "target_regime_rows_in_segments": int(target_regime_rows_in_segments),
        "dropped_missing_rows_in_variables": int(dropped_missing_rows),
        "scaler_mean": [float(value) for value in scaler.mean_],
        "scaler_scale": [float(value) for value in scaler.scale_],
    }
    return dataframe, metadata


def run_target_masked_pcmci(
    dataframe: pp.DataFrame,
    tau_min: int,
    tau_max: int,
    pc_alpha: float,
    alpha_level: float,
    fdr_method: str,
    mask_type: str,
) -> dict[str, Any]:
    try:
        cond_ind_test = ParCorr(significance="analytic", mask_type=mask_type)
    except TypeError:
        cond_ind_test = ParCorr(significance="analytic")
        if hasattr(cond_ind_test, "set_mask_type"):
            cond_ind_test.set_mask_type(mask_type)
        else:
            setattr(cond_ind_test, "mask_type", mask_type)

    pcmci = PCMCI(
        dataframe=dataframe,
        cond_ind_test=cond_ind_test,
        verbosity=0,
    )
    try:
        return pcmci.run_pcmci(
            tau_min=tau_min,
            tau_max=tau_max,
            pc_alpha=pc_alpha,
            alpha_level=alpha_level,
            fdr_method=fdr_method,
        )
    except TypeError:
        return pcmci.run_pcmci(
            tau_max=tau_max,
            pc_alpha=pc_alpha,
            alpha_level=alpha_level,
            fdr_method=fdr_method,
        )


